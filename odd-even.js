// *has return + has parameter
function odd_even(number){
    if(number % 2 == 0){
        console.log("the number is even")
        return "even";
    }
    else if(number % 2 != 0){
        console.log("the number is odd")
        return "odd"
    }
   
}
var number = 62;

var myNumber = odd_even(number);
console.log(myNumber);



